
public class RecordResult {
	
		 int numofcir; //the number of circulation;		 
		 int seqport; //the sequence of port;  
		 int numofsta; //the number of used stacks;	
		public RecordResult(int numofcir, int seqport, int numofsta) {
			super();
			this.numofcir = numofcir;
			this.seqport = seqport;
			this.numofsta = numofsta;
		}
		public int getNumofcir() {
			return numofcir;
		}
		public void setNumofcir(int numofcir) {
			this.numofcir = numofcir;
		}
		public int getSeqport() {
			return seqport;
		}
		public void setSeqport(int seqport) {
			this.seqport = seqport;
		}
		public int getNumofsta() {
			return numofsta;
		}
		public void setNumofsta(int numofsta) {
			this.numofsta = numofsta;
		}
		 
		 
	
	

}
